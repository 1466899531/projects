"""
     解析接口响应内容为字典- JSON字符串
"""


def parse_json(s):
    # 方法查找指定值的第一次出现
    begin = s.find('{')
    # 方法查找指定值的最后一次出现
    end = s.rfind('}') + 1
    # 截取中间部分 转为字典
    return json.loads(s[begin:end])


