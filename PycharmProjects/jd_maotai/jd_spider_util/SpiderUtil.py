"""
    通过第三方的Server酱的推送服务,将结果发给微信设备通知
"""


def send_wechat(message):
    # server酱的服务地址。获取全局配置global_config参数sendKey
    url = 'https://sc.ftqq.com/{}.send'.format(global_config.getRaw('messenger', 'sendKey'))
    payload = {
        "text": '抢购结果',
        "desp": message
    }
    # 获取全局配置global_config参数DEFAULT_USER_AGENT
    headers = {
        'User-Agent': global_config.getRaw('config', 'DEFAULT_USER_AGENT')
    }
    # 调用server酱的接口地址，发送消息给微信
    requests.get(url, params=payload, headers=headers)


"""
    接口响应结果 True成功，False失败
"""


def response_status(resp):
    if resp.status_code != requests.codes.OK:
        print('Status: %u, Url: %s' % (resp.status_code, resp.url))
        return False
    return True


"""
    通过脚本, 打开图片
"""


def open_image(image_file):
    if os.name == "nt":
        os.system('start ' + image_file)  # for Windows
    else:
        if os.uname()[0] == "Linux":
            if "deepin" in os.uname()[2]:
                os.system("deepin-image-viewer " + image_file)  # for deepin
            else:
                os.system("eog " + image_file)  # for Linux
        else:
            os.system("open " + image_file)  # for Mac


"""
    保存图片
"""

def save_image(resp, image_file):
    with open(image_file, 'wb') as f:
        # 将文本流保存到文件
        for chunk in resp.iter_content(chunk_size=1024):
            f.write(chunk)
"""
    格式时间成毫秒
"""
def str_data_to_num(str_data):
    strTime = time.strptime(str_data, "%Y-%m-%d %H:%M:%S")
    mkTime = int(time.mktime(strTime) * 1000)
    return mkTime

"""
    格式毫秒成指定格式时间
"""
def num_to_str_data(str_data):
    str_data = str_data / 1000
    str_data = time.localtime(str_data)  # 生成一个元祖式的时间
    strfTime = time.strftime("%Y-%m-%d %H:%M:%S", str_data)  # 格式化元祖
    return strfTime

